package Ex_04_Operators;

public class Lab037_Arthimatic_Operator {
    public static void main(String[] args) {

        // Arithmetic Operator
        // + --> Addition
        // - --> Subraction
        // * --> Multiplication
        // / --> Division
        // % --> Modulo --> Modulus reminder

        int a = 37;
        int b = 12;
        float c = 10f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a/c);





    }
}
